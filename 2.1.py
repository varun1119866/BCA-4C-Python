# Define a sample string
sample_string = "Hello, World!"

# Demonstrate slicing operations
print("Original string:", sample_string)

# Slicing to get a substring from index 2 to index 5
print("Substring from index 2 to index 5:", sample_string[2:6])

# Slicing to get the first 5 characters
print("First 5 characters:", sample_string[:5])

# Slicing to get the last 5 characters
print("Last 5 characters:", sample_string[-5:])

# Slicing to get every second character
print("Every second character:", sample_string[::2])

# Slicing to reverse the string
print("Reversed string:", sample_string[::-1])
