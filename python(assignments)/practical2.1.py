# Slicing operations in strings

text = "Hello, World!"

# Print the first 5 characters
print("First 5 characters:", text[:5])

# Print characters from index 7 to 11 (inclusive)
print("Characters from index 7 to 11:", text[7:12])

# Print every other character
print("Every other character:", text[::2])

# Print the string in reverse
print("Reversed string:", text[::-1])
